/**
 * Sum two values
 */
export function add(a:number, b:number):number {
  return a + b;
}

/**
 * Diff two values
 */
 export function sub(a:number, b:number):number {
  return a - b;
}

/**
 * Division two values
 */
 export function div(a:number, b:number):number {
  return a / b;
}

/**
 * multiple two values
 */
 export function multiple(a:number, b:number):number {
  return a * b;
}

